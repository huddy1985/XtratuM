#ifndef _ULIBC_STDDEF_H_
#define _ULIBC_STDDEF_H_

#include <sys/types.h>

#undef NULL
#define NULL ((void *) 0)

#undef offsetof
#ifdef __compiler_offsetof
#define offsetof(TYPE,MEMBER) __compiler_offsetof(TYPE,MEMBER)
#else
#define offsetof(TYPE, MEMBER) ((size_t) &((TYPE *)0)->MEMBER)
#endif

#endif
